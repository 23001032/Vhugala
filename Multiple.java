public class Multiple{
    public static void main(String args[]){
        int num=6;
        if(num%2==0 && num%3==0){
            System.out.println(num+" is a multiple of 2 and 3");
        }
        else{
             System.out.println(num+" is not a multiple of 2 and 3");
        }
    }
    
}
